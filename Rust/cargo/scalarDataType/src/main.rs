fn main() {
    // scalar data types 
    // 1) Integer 
    // 2) Floating point number 
    // 3) Boolean 
    // 4) Character
    
    // Integer 
    let number:i64 = 1234;
    let number2 = 1245;
    println!("The value of Number {number}");
    println!("The value of number2 {number2}");

    // floating point number 
    let f_number:f64 = 1234.340;
    let f_number2 = 1234.2340;

    println!("The value of floating point number1 is : {f_number}");
    println!("The value of floating point number2 is : {f_number2}"); 

    // character 

    let a_char:char = 'G';
    let b_char = 'A';

    println!("This is a_char {a_char}");
    println!("This is b_char {b_char}");
    
    // Boolean 
    let a_bool:bool = true;
    let b_bool = false;

    println!("This is a_bool {a_bool}");
    println!("This is b_bool {b_bool}");
    


}