#include<string>
#include<iostream>
using namespace std;
int main () {
    // string to integer using stoi function 
    string s = "12345678";
    int x;
    x = stoi(s);
    cout << x << endl;

    // string to long long
    // using stoll function 

    string g = "123487903434190";
    long long y ;
     y = stoll(g);
     cout << y ;

}