// square root function 
// power funtion 
// and cmath library
#include<iostream>
#include<cmath>

using namespace std;
int main() {
    int num1 , num2;
    cout<<"Enter the value of num1 : ";
    cin>>num1;
    cout<<"Enter the value of num2 : ";
    cin>>num2;
    cout<<"Square root of num1 : "<<sqrt(num1)<<endl;
    cout<<num1<<" raised to "<<num2<<" is : "<<pow(num1,num2)<<endl;
    
}