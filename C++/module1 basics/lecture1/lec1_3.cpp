#include<iostream>
using namespace std;
int main() {
    // we have + , - , * , /  operators 
    // + is used for adding two elemets 
    // - is used for subtracting two elements 
    // * is used for multiplying two elements 
    // / is used for dividing two elements

    // example ----> 
    int num1 = 10 ;
    int num2 = 20 ;

    cout<<num1 + num2 << endl; // 30
    cout<<num1 - num2 << endl; // -10
    cout<<num1 * num2 << endl; // 200
    cout<<num1 / num2 << endl; // 0

}